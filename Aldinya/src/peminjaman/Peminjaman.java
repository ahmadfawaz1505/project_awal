    private void editStok() {
        try {
            String dataStok = null;
            String stoknya = null;
            java.sql.Connection conn = (Connection) koneksi.configDB();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from item where nama='" + namaItemForm.getText() + "'");
            while (rs.next()) {
                dataStok = rs.getString("stok");
            }

            java.sql.Connection conn2 = (Connection) koneksi.configDB();
            Statement st2 = conn2.createStatement();
            ResultSet rs2 = st2.executeQuery("select * from peminjaman where ktp='" + ktpForm.getText() + "'");
            while (rs2.next()) {
                stoknya = rs2.getString("jumlah");
            }

            int hitung = Integer.parseInt(dataStok) + Integer.parseInt(stoknya);
            int kurangiStok = hitung - Integer.parseInt(jumlahForm.getText());
            String sql2 = "UPDATE item SET stok = '" + kurangiStok + "' WHERE id='" + idItemForm.getText() + "'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql2);
            pst.execute();
        } catch (Exception e) {
        }
    }

