    private void tombolEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolEditActionPerformed
        if (idItemForm.getText().isEmpty() || namaItemForm.getText().isEmpty() || namaForm.getText().isEmpty() || ktpForm.getText().isEmpty() || hpForm.getText().isEmpty() || alamatForm.getText().isEmpty() || peminjamanForm.getText().isEmpty() || pengembalianForm.getText().isEmpty() || jumlahForm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih Data Dulu");
        } else {
            editStok();
            try {
                String sql = "UPDATE peminjaman SET id_item = '" + idItemForm.getText() + "', nama_item = '" + namaItemForm.getText() + "', nama = '" + namaForm.getText() + "', ktp= '" + ktpForm.getText() + "', hp= '" + hpForm.getText() + "', alamat= '" + alamatForm.getText() + "', tgl_pinjam= '" + peminjamanForm.getText() + "', tgl_kembali= '" + pengembalianForm.getText() + "', jumlah= '" + jumlahForm.getText() + "', tot_harga= '" + totalForm.getText() + "' WHERE ktp='" + ktpForm.getText() + "'";
                java.sql.Connection conn = (Connection) koneksi.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();

                JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Perubahan Data Gagal" + e.getMessage());
            }
        }
        reset();
        loadTable();
    }//GEN-LAST:event_tombolEditActionPerformed

